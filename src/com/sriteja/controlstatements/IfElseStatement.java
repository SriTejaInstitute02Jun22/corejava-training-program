package com.sriteja.controlstatements;

public class IfElseStatement {

	//main method
	public static void main(String agrs[]) {
		
		//variable declaration
		int x =20;
		int y = 45;
		
		//if else statement
		if(50<=50) {	//true
			System.out.println("If condition is true");
		}else {
			System.out.println("If condition is false");
		}
		
		System.out.println();
		//if else statement with false condition
		if(x>=y) {	//false
			System.out.println("Second if condition is true");
		}else {
			System.out.println("Second if condition is false");
		}
	}
}
