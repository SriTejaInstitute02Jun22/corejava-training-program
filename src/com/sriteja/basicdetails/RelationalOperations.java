package com.sriteja.basicdetails;

public class RelationalOperations {

	public static void main(String args[]) {
		
		//Relational Operations <, >, <=, >=, instanceof
		
		int i=10;
		int j=20;
		
		System.out.println(i<j);//10<20		-> true
		System.out.println(i>j);//10>20		-> false
		System.out.println(i<=j);//10<=20	-> true
		System.out.println(i>=j);//10>=20	-> false
		System.out.println(i==j);//10==20	-> false
		System.out.println(i!=j);//10!=20 	-> true
	}
}
